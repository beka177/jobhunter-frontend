// Context + Provider + Hook
// =========================================================
const I18nContext = createContext(null);

export function I18nProvider({ children }) {
  const [lang, setLangState] = useState(() => {
    try {
      const saved = localStorage.getItem(STORAGE_KEY);
      if (saved === 'ru' || saved === 'kk') return saved;
    } catch (e) { /* ignore */ }
    return DEFAULT_LANG;
  });

  useEffect(() => {
    try { localStorage.setItem(STORAGE_KEY, lang); } catch (e) { /* ignore */ }
    document.documentElement.lang = lang === 'kk' ? 'kk' : 'ru';
  }, [lang]);

  const setLang = (newLang) => {
    if (newLang === 'ru' || newLang === 'kk') setLangState(newLang);
  };

  const t = (key, vars) => {
    const bundle = DICT[lang] || DICT[DEFAULT_LANG];
    let raw = bundle[key];
    if (raw === undefined) {
      // Fallback to RU, then to key itself
      raw = DICT[DEFAULT_LANG][key];
      if (raw === undefined) return key;
    }
    if (vars) {
      return raw.replace(/\{(\w+)\}/g, (_, k) => (vars[k] !== undefined ? vars[k] : `{${k}}`));
    }
    return raw;
  };

  return (
    <I18nContext.Provider value={{ t, lang, setLang }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useT() {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    // Без провайдера — возвращаем безопасный fallback на ключи
    return { t: (k) => k, lang: DEFAULT_LANG, setLang: () => {} };
  }
  return ctx;
}
